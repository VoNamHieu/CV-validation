import type { CVData } from './types';

/* ─── XSS-safe HTML escaping ─── */
export function escapeHtml(str: string): string {
    return (str ?? '')
        .replace(/&/g, '&amp;')
        .replace(/</g, '&lt;')
        .replace(/>/g, '&gt;')
        .replace(/"/g, '&quot;')
        .replace(/'/g, '&#039;');
}

/* ─── Render a description string (newline-separated bullets) into HTML ─── */
function bulletsToHtml(text: string): string {
    if (!text) return '';
    const lines = text
        .split('\n')
        .map((l) => l.replace(/^\s*[-*•]\s*/, '').trim())
        .filter(Boolean);
    if (lines.length === 0) return '';
    if (lines.length === 1) return `<p class="single-line">${escapeHtml(lines[0])}</p>`;
    return `<ul class="bullets">${lines.map((l) => `<li>${escapeHtml(l)}</li>`).join('')}</ul>`;
}

