/* ─── Suggested filename, sanitized for download ─── */
export function buildCvFileName(cv: CVData, jobTitle?: string): string {
    const name = (cv.name || 'cv').replace(/[^A-Za-z0-9_\- ]/g, '').trim().replace(/\s+/g, '_');
    const job = (jobTitle || 'optimized').replace(/[^A-Za-z0-9_\- ]/g, '').trim().replace(/\s+/g, '_');
    return `${name || 'cv'}_${job || 'optimized'}`.slice(0, 80);
}
